<?php

namespace Laravel\Passport\Bridge;

trait UserAwareTrait
{
    /**
     * @var User $user
     */
    protected $user;

    /**
     * @return User
     */
    public function getUser()
    {
        return $this->user;
    }

    /**
     * @param User $user
     */
    public function setUser($user)
    {
        $this->user = $user;
    }
}
